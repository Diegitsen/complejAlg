void PintarMapa()
{
	for (int f = 0; f < MaxFilas; f++)
	{
		for (int c = 0; c < MaxCol; c++)
		{
			if (Mapa[f][c] == 1)
			{
				Console::ForegroundColor = ConsoleColor::Black;
				cout << char(219);
			}
			if (Mapa[f][c] == 3)
			{
				Console::ForegroundColor = ConsoleColor::DarkBlue;
				cout << char(219);
			}
			if (Mapa[f][c] == 2)
			{
				Console::ForegroundColor = ConsoleColor::Red;
				cout << char(79);
			}
		}
		cout << "\n";
	}
}

void dibujarPacman(int px, int py)
{
	gotoxy(px, py);
	Console::BackgroundColor = ConsoleColor::Black;
	Console::ForegroundColor = ConsoleColor::Yellow;
	cout << char(1);
}

void borrarPacman(int px, int py)
{
	gotoxy(px, py);
	cout << " ";
}

void moverPacman(int &px, int &py)
{
	if (kbhit())
	{
		char tecla = getch();
		borrarPacman(px, py);
		if (tecla == ARRIBA && (Mapa[py - 1][px] == 1 || Mapa[py - 1][px] == 2))
		{
			if (Mapa[py - 1][px] == 2)
				contadorPuntos += 5;
			py--;
		}
			

		if (tecla == ABAJO && (Mapa[py + 1][px] == 1 || Mapa[py + 1][px] == 2))
		{
			if (Mapa[py + 1][px] == 2)
				contadorPuntos += 5;
			py++;
		}
			

		if (tecla == DERECHA && (Mapa[py][px + 1] == 1 || Mapa[py][px + 1] == 2))
		{
			if (Mapa[py][px + 1] == 2)
				contadorPuntos += 5;
			px++;
		}
			

		if (tecla == IZQUIERDA && (Mapa[py][px - 1] == 1 || Mapa[py][px - 1] == 2))
		{
			if (Mapa[py][px - 1] == 2)
				contadorPuntos += 5;
			px--;
		}

		dibujarPacman(px, py);
	}
}

