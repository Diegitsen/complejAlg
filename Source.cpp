void gotoxy(int x, int y)
{
	HANDLE Identificador;
	Identificador = GetStdHandle(STD_OUTPUT_HANDLE);

	COORD dwPos;
	dwPos.X = x;
	dwPos.Y = y;
	SetConsoleCursorPosition(Identificador, dwPos);
}

void dibujarVidas(int px, int py)
{
	for (int i = 0; i < vidas; i++)
	{
		gotoxy(px + i, py);
		cout << char(3);
	}
}

